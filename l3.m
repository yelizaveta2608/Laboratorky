fprintf('Сумма квадратов отклонений, S=%4.4f \n',S)


W=(12*S)/((N_oper^2)*((N_fact^3)-N_fact))           ; % Найти Коэффициент конкордации
fprintf('Коэффициент конкордации, W=%4.4f \n',W)






